import type { HittingMovementType, HittingBatPathType } from './hitting-identity-data';
import type { MechanicalIssue } from './hitting-mechanical-diagnostic-data';

/* ─── Types ──────────────────────────────────────── */

export type DrillRole = 'primary' | 'support' | 'reset' | 'foundation';
export type AgeGroup = 'youth' | 'all';
export type DrillDifficulty = 'beginner' | 'intermediate' | 'advanced';
export type DrillPhase = 'foundation' | 'development' | 'performance';
export type DrillCategory =
  | 'foundations'
  | 'timing'
  | 'forward-move'
  | 'posture'
  | 'direction'
  | 'barrel-turn'
  | 'connection'
  | 'extension';

export interface DrillMeta {
  id: string;
  name: string;
  category: DrillCategory;
  /** Which diagnostic issues this drill targets */
  mechanicalFocus: MechanicalIssue[];
  /** Quick fix categories this drill belongs to */
  issues: string[];
  /** @deprecated — use movementAffinity (single value) instead */
  moverAffinity: MoverType[];
  /** Movement pattern affinity (single value or 'any') */
  movementAffinity: HittingMovementType | 'any';
  /** Bat path affinity (single value or 'any') */
  batPathAffinity: HittingBatPathType | 'any';
  difficulty: DrillDifficulty;
  phase: DrillPhase;
  equipment: string[];
  /** 'youth' = age ≤ 14 only, 'all' = any age */
  ageGroup: AgeGroup;
  /** What role(s) this drill can serve in a recommendation stack */
  roles: DrillRole[];
}

